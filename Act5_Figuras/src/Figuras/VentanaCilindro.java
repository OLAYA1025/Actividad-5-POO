    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        boolean error=false;
        double Radio=0;
        double Altura=0;
        try{
            Radio=Double.parseDouble(this.txtRadio.getText());
            Altura=Double.parseDouble(this.txtAltura.getText());
            Cilindro cilindro=new Cilindro(Radio, Altura);
            this.LabelVolumen.setText("Volumen (cm3): "+cilindro.CalcularVolumen());
            this.LabelSuperficie.setText("Superficie (cm2): "+cilindro.CalcularSuperficie());
        }catch(Exception e){
            error=true;
        }
        if (error){
            JOptionPane.showMessageDialog(null, "Campo nulo o Error en formato de n√∫mero",
                    "Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

